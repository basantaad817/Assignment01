//Write a program to print numbers from 1 to 10.

public class Q_01 {
    public static void main(String[] args) {

        //For loop
        for (int i=1;i<=10;i++) {
            System.out.println(i);
        }

        //While loop
        int j=1;
        while (j<=10) {
            System.out.println(j);
            j++;
        }

        //Do-While loop
        int k=1;
        do {
            System.out.println(k);
            k++;
        } while (k<=10);
    }
}
